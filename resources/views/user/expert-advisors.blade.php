                <p>
                    <em>没有经验的新手总是想找一个交易机器人完成所有的工作, 因此交易员不需要动一根手指. 这个想法一直被所有计算机时代的交易员所追求.&nbsp; 决策责任的负担消耗了交易员的神经, 现在正好指向交易外汇专家顾问.</em></p>
                <h3>
                    什么是外汇专家顾问?</h3>
                <p>
                    外汇专家顾问是一个程序, 它能够按照交易员的说明在终端执行任何行动, 没有他的直接参与. 所有的任务将被自动或者机械地执行, 这就是为什么顾问被称为专家或者机械交易系统 (MTS). 简单的说, 这是一个程序发送应用到经纪商不需要交易员的任何干预. 您安装一个利润外汇顾问专家到目前外汇在线交易平台, 它是连接到经纪商服务器, 调整所有的设置, 顾问将按照预置策略开始进行交易.</p>
                <h3>
                    外汇顾问专家的好处</h3>
                <p>
                    从心理学的角度来看, 外汇专家顾问是不可替代的. 一个交易员减少了决策的责任, 使交易变得没那么紧张. 这位交易员不需要有深入的技术知识和基础分析, 因为所有的计算都已经包含在程序里. 此外, 甚至当交易员缺席工作场所顾问能够处理交易信号.</p>
                <h3>
                    编写一个外汇专家顾问</h3>
                <p>
                    MetaTrader4的外汇专家顾问是使用 MQL4 编程语言. 这种语言是为编写专家顾问由交易终端的制造商专门开发的. 它允许交易员编制交易系统程序没有任何困难, 它将以在线模式日夜交易. 熟悉这种语言的程序员做这些工作将没有任何困难. 对于普通用户来说这将是一个更艰巨的任务.</p>
                <h3>
                    外汇专家顾问指标</h3>
                <p>
                    为了顾问更有效的工作您可以创建自己的技术指标. 它们将是MetaTrader4 终端目前指标的一个更好的补充. 使用顾问指标的目的是实施解析功能并生成交易信号.</p>
                <h3>
                    内置和自己的外汇专家顾问</h3>
                <p>
                    MetaTrader4 交易终端有几个内置的专家顾问. 它们有一个独立交易系统功能和配音交易信号. 它们在初学者和 MetaTrader 4 客户端编程能力的专门演示是非常受欢迎的.</p>
                <p>
                    创建自己的顾问可能性是如此惊人的简单. 您可以以价格和时间设置各种命令, 自动打开相反的订单, 等等. 这些程序能够在他们的工作场所取代交易员.</p>
                <h3>
                    测试外汇专家顾问</h3>
                <p>
                    交易终端不仅可以编写顾问, 而且可以使用以前的历史数据检查它们, 这是使用顾问的另一个特性. 测试是非常有用的, 因为它有助于测量机械交易系统在历史数据上的能效, 估计未来收益的机会和错误. <span class="rvts13">如果您已经测试了顾问, 并知道它在不同的市场条件下如何表现, 您就可以开始交易,不需要进行干预. </span>为了这个目的, 终端有一个专门的窗口可以输入顾问的参数进行优化.</p>
                <h3>
                    临别赠言初学者</h3>
                <p>
                    初学者可能会认为顾问交易是非常复杂的. 它并非如此. 大约一个月后任何交易员可以开始自己的自动交易系统. 即使您犹豫使您的资金到一个计算机程序, 您可以设置顾问的5个声音提醒, 很大程度上方便了您的工作, 这样您花更少的时间在图形分析上, 期待一个来打开和关闭位置的信号.</p>
                <p>
                    您可以在互联网上找到很多的顾问, 但是要找到一个盈利的非常困难, 并且使用每个信号是非常累的, 也有可能导致耗尽您的存款. 这就是为什么我们建议您不买您遇到的第一个顾问. 许多顾问在测试一个货币对时展示良好的结果, 但是其他的表现不佳. 最好是使用那些您已经测试过的顾问工具.</p>
