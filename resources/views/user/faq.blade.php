                <a href="javascript:void(0)" onClick="showFaqText(28);" >如何计算一个点的成本?</a>
                <div id="faq_text28" class="fl-answer">

                    <div><div>
                            <div>
                                <strong>公式:</strong> &lt;1个点的成本&gt; = (合约* (价格 + 1个点)) - (合约 * 价格),</div>
                            <div>
                                &nbsp;</div>
                            <div>
                                <strong>例如</strong>: 计算欧元兑英镑的1个点的成本。交易账户的货币是美元.</div>
                            <div>
                                &nbsp;</div>
                            <div>
                                一个交易者开放头寸:</div>
                            <div>
                                &nbsp;</div>
                            在0.8365价位买入1手EURGBP<br />
                            合约大小=100,000<br />
                            价格=0.8365<br />
                            1个点=0.0001
                            <ol>
                                <li>
                                    <div>
                                        1个点的成本 &laquo;买入1手 EURGBP 0.8365&raquo; = (100.00 * (0.8365 +0,0001)) - (100.000 * 0.8365) = 10 GBP;</div>
                                </li>
                                <li>
                                    <div>
                                        由于交易账户的货币是USD, 账户损益用 USD计算. 因此, 用GBP计算的1个点成本应该转化成USD;</div>
                                </li>
                                <li>
                                    <div>
                                        10 GBP * GBP / USD 汇率 = 10 * 1.5417 = 15.47 USD.</div>
                                </li>
                            </ol>
                            <div>
                                &nbsp;</div>
                            <div>
                                因此，1个点&laquo;买入1手EURGBP 0.8365&raquo;的成本是15.47 USD.</div>
