                            <margin><contract size=""><leverage> </leverage></contract></margin></p>
                        <p style="text-align: justify;">
                            <strong>基础货币</strong>是前面货币的报价，例如:</p>
                        <ul>
                            <li style="text-align: justify;">
                                在欧元兑美元 基础货币是欧元;</li>
                            <li style="text-align: justify;">
                                在美元兑瑞郎 基础货币是美元;</li>
                            <li style="text-align: justify;">
