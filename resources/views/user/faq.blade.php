                            <strong>注意:</strong><strong> </strong>为了简化保证金需求计算,我们推荐使用交易者的 <a href="/analytics/forex-calculator/" target="_blank">计算器</a>.</p>
                        <p style="text-align: justify;">
                            <strong>计算基础货币保证金的公式<strong>:</strong></strong></p>
                        <p style="text-align: justify;">
                            <margin> </margin></p>
