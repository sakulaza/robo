                        </ul>
                        <p style="text-align: justify;">
                            <strong>合约大小</strong><strong> </strong>是基础货币合约的数量。1手的价值通常是100,000个基础货币单位。因此，0.1lot=100,000 * 0.1 = 10,000 个基础货币单位，0.01lot=100,000 * 0.01 = 1,000 个基础货币单位;</p>
                        <p style="text-align: justify;">
                            <strong>杠杆<strong> </strong></strong>是交易者自己的资金和借入资金的比例，例如:</p>
                        <ul>
                            <li style="text-align: justify;">
                                1:500 杠杆 -- 500;</li>
                            <li style="text-align: justify;">
                                1:100 杠杆 -- 100.</li>
                        </ul>
                        <p style="text-align: justify;">
                            计算基础货币保证金之后，转换成投资货币（根据当时的汇率）--美元，欧元 等等.</p>
                        <p style="text-align: justify;">
                            <strong>举例说明:</strong></p>
                        <p style="text-align: justify;">
                            一个交易者使用1：500的杠杆在1.2457价位买入1手EURUSD 。这个交易的保证金计算如下:</p>
                        <ul>
                            <li style="text-align: justify;">
                                对于在1.2457价位买入1手EURUSD头寸，基础货币是欧元。因此，这个头寸的保证金应该是100,000/500=200欧元;</li>
                            <li style="text-align: justify;">
                                如果交易帐户的货币是不同于欧元的,那么用欧元计算保证金必须转换成交易帐户的货币;</li>
