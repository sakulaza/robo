        <h1 >什么是外汇交易</h1>
        <div ><p>
                <b>外汇市场，也称为&ldquo;FOREX&rdquo;或&ldquo;FX&rdquo;市场</b> ,是全球最大的金融市场，其日交易量达到 4 兆美元。巨大的交易量，使得任何一个机构都没有能力操纵这一市场，价格的走动有规律可循，对普通的投资者比较公平.</p>
            <p>
                除此之外，与期货和证券市场相比，外汇市场与它们有一点本质的区别，这点区别同时也是其它市场的局限性&mdash;&mdash;交易在一天结束时被中断，只有在第二天早上重新开始。因此，如果您是在俄国市场进行交易，而有些与市场相关的重要事件在美国发生了，当早上开市的时候，可能与您所希望的会完全不一样. <b>外汇市场每天24小时不停工作</b>，外币交易在一周内的工作日中不会中断。几乎在每个时区（伦敦，纽约，东京，香港，悉尼等）都有愿意对外汇提供报价的交易商。</p>
            <p>
                从1971年直到几年前，这个市场的真正投资者是银行，跨国公司和大型经纪公司。如果独立个人希望把资金投入到该市场，那么他应该同银行合作，投入大约一百万美元的资金，以满足获得五百万到一千万美元交易的需要。在经纪商那情况稍微好一些，他们可以把最低保证金存款降低到二十五万美元。但是今天，外汇交易市场已经对小型投资者开放。与先前银行和经纪商要求的巨额资金相比，现在的保证金需求已经大大地降低了，终于，使得众多个人可以与参与到这个市场,而小的投资者可以利用互联网的优势，而在过去，它只可能被大型参与者所使用。</p>
            <p>
                <b>了解外汇交易</b></p>
            <p>
                外汇交易是指投资者利用货币之间兑换率的变动而进行的买卖。您可以通过我们提供的网上外汇交易平台直接进行外汇交易。对于投资者来说，最好的机会总是交易那些交易量最大的货币，即&ldquo;主要货币 &rdquo;。今天，85% 的外汇交易是主要货币，包括<b>美元</b>，<b>日元</b>，<b>欧元</b>，<b>英镑</b>，<b>瑞郎</b>，<b>加元</b>和<b>澳元</b>.</p>
            <p>
                外汇是以货币对形式交易，每一种货币，都有国际统一的符号，例如：欧元/美元 (EUR/USD) 或美元/日元 (USD/JPY)。&ldquo;外汇交易&rdquo;是买入一种货币，同时卖出另外一种货币。比如，您预测欧元/美元 (EUR/USD) 将升值，则您会买入 EUR/USD，意味着您买入了欧元，同时卖出了美元.</p>
            <p>
                <b>为什么外汇市场吸引投资者?</b></p>
            <b>流动性</b>;外汇市场每天交易所吸收的资金远远地超过了其它任何市场的数量。高度流动性是对投资者最强大的吸引力，它使得投资者可以按当前的市场报价自由开仓和平仓，而且几乎没有数量的限制。
            <p>
                <b>不间断的运转</b>;对市场参与者根本性的吸引力是它的每天24小时不间断运转。在外汇市场，参与者不象在其它很多别的市场，需要进行等待才能对各种发生事件作出反映，而在外汇市场不需要.</p>
            <p>
                <b>灵活的操作</b>;在外汇市场，交易人可以用一个希望的定期期限建一个新的定单.</p>
            <p>
                <b>低廉的交易费用</b>;外汇交易没有各种<b>佣金</b>，除了自然的市场价差 bid/ask.</p>
            <p>
                <b>确切的报价</b>;由于市场高度的流动性，大多数的交易可能是按统一市场价执行的。这避免了期货中的不稳定性和其它货币投资中，在某个时间只可以按市场价交易一定数量的资金.</p>
            <p>
                <b>网络技术的发展提升了行业的发展</b></p>
            <p>
                在以前，外汇交易一直由大型银行和其他金融机构独享，个人投资者基本无缘这一市场。但从上世纪 90 年代末期，互联网的迅速发展和普及，极大的降低了投资门槛及成本，使个人参与外汇投资成为可能。</p>
            <p>
                原来只有金融机构才能付得起成本的道琼斯新闻、路透社行情、专业图表工具等，现在只要开户就可以免费使用。</p>
            <p>
                原来必须到银行才能完成的交易行为，现在坐在家里或者办公室，轻轻点击鼠标就可以在几秒钟内完成。</p>
